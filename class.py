class dogs:
    def _init_(self, name, color, age):
        self.name = name
        self.color = color
        self.age = age

dog1 = dogs("Dawa","brown",8)
dog2 = dogs("Nima","Red",9)
dog3 = dogs("Blacky","Black",10)

print(dog1.name.color.age)
print(dog2.name.color.age)
print(dog3.name.color.age)
